'''
Sudo 코드
1. 입력값 N, M을 받는다.
2. 만약 N == 1이면 방문할 수 있는 칸 수는 1이다.
3. 만약 N == 2이면 방문할 수 있는 칸 수는 min(4, (M + 1) // 2) 이다.
4. 만약 M < 7이면 방문할 수 있는 칸 수는 min(4, M) 이다.
5. 그렇지 않다면, 방문할 수 있는 칸 수는 M - 2 이다.
6. 결과를 출력한다.

- 이동 횟수 ≥ 4 → 4가지 이동을 모두 사용해야 함.
- 이동 횟수 < 4 → 제약 없음.
'''
# 입력 처리
N, M = map(int, input().split())

# 1. 세로 크기(N)에 따른 패턴
if N == 1:
    # 세로 이동 불가 → 한 칸만 방문 가능
    print(1)

elif N == 2:
    # 두 방향만 사용 가능 → M이 커질수록 이동 가능
    print(min(4, (M + 1) // 2))

elif N >= 3:
    if M < 7:
        # 세로 이동은 가능하지만 M이 7보다 작으면 4칸 이상 못 감
        print(min(4, M))
    else:
        # 4가지 이동 모두 사용 가능 → M-2
        print(M - 2)
